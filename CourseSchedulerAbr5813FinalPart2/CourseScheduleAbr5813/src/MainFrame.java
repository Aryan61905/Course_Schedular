    private void AdminDropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdminDropStudentButtonActionPerformed
        // TODO add your handling code here:
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        ArrayList<StudentEntry> students =StudentQueries.getAllStudents();
        String studentID = students.get(DropStudentSelectStudentCombobox.getSelectedIndex()).getStudentID();
        String firstname =students.get(DropStudentSelectStudentCombobox.getSelectedIndex()).getFirstName();
        String lastname =students.get(DropStudentSelectStudentCombobox.getSelectedIndex()).getLastName();
        StudentQueries.dropStudent(studentID);
        DropStudentTextArea.append(firstname + ","+lastname+" "+studentID+" has been dropped from the list of students.");
        rebuildStudentComboBoxes();
        
        
        for (String s: semesters){
            ArrayList<ScheduleEntry>schedule = ScheduleQueries.getScheduleByStudent(s,studentID);
            DropStudentTextArea.append("\n For Smester "+s+"\n");
            for (ScheduleEntry entry:schedule){
                String status = entry.getS();
                
                String CourseCode =entry.getCoursecode();
                ArrayList<ScheduleEntry> waitlisted =ScheduleQueries.getWaitlistedStudentsByCourse(s, CourseCode);
                if ("Scheduled".equals(status)){
                    ScheduleQueries.dropStudentScheduleByCourse(s, entry.getStudentID(), entry.getCoursecode());
                    DropStudentTextArea.append(firstname+" "+lastname+" "+" has been dropped from"+ CourseCode+"\n");
                
                
                if (!waitlisted.isEmpty()){
                    ScheduleQueries.updateScheduleEntry(s,waitlisted.get(0));
                    StudentEntry waitlist = StudentQueries.getStudent(waitlisted.get(0).getStudentID());
                    
                    DropStudentTextArea.append(waitlist.getFirstName()+","+waitlist.getLastName()+" "+waitlist.getStudentID()+"has been scheduled into"+CourseCode+"\n");
                    
                
                } 
            }
            else{
                    ScheduleQueries.dropStudentScheduleByCourse(s, entry.getStudentID() , entry.getCoursecode());
                    DropStudentTextArea.append(firstname+","+lastname+" "+studentID+" has been dropped from the waitlist for"+CourseCode+"\n");
                    }
            
            }}
    }//GEN-LAST:event_AdminDropStudentButtonActionPerformed

