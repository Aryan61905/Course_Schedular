    private void displayClassesDisplayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayClassesDisplayButtonActionPerformed
        // TODO add your handling code here:
        String semester = currentSemesterComboBox.getSelectedItem().toString();
        String allCourseCodes = SelectCourseComboBox.getSelectedItem().toString();
        ArrayList<StudentEntry>students=StudentQueries.getAllStudents();
        String studentID  =students.get(SelectStudentComboBox.getSelectedIndex()).getStudentID();
        ArrayList<ScheduleEntry>studentSchedule=ScheduleQueries.getScheduleByStudent(semester, studentID);
        String firstName = students.get(SelectStudentComboBox.getSelectedIndex()).getFirstName();
        String lastName = students.get(SelectStudentComboBox.getSelectedIndex()).getLastName();
        int seats =CourseQueries.getCourseSeats(semester,allCourseCodes);
        int returnseats = ScheduleQueries.getScheduledStudentCount(semester, allCourseCodes);
        Timestamp timestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        if (seats > returnseats){
            ScheduleEntry schedule = new ScheduleEntry(semester,allCourseCodes,studentID,"Scheduled",timestamp);
            ScheduleCourseLabel.setText(lastName+","+firstName+" has been added.");
            ScheduleQueries.addScheduleEntry(schedule);
        }
        else{
            ScheduleEntry schedule= new ScheduleEntry(semester,allCourseCodes,studentID,"Waitlisted",timestamp);
            ScheduleQueries.addScheduleEntry(schedule);
            ScheduleCourseLabel.setText(lastName+","+firstName+" has been waitlisted for the class.");
        }
    }//GEN-LAST:event_displayClassesDisplayButtonActionPerformed

